// Existing utility functions remain the same
export function getConfig(path) {
    return getNestedValue(APP_CONFIG, path);
}

export function setConfig(path, value) {
    setNestedValue(APP_CONFIG, path, value);
}

export function mergeConfig(config) {
    Object.keys(config).forEach(key => {
        if (typeof config[key] === 'object' && config[key] !== null) {
            APP_CONFIG[key] = { ...APP_CONFIG[key], ...config[key] };
        } else {
            APP_CONFIG[key] = config[key];
        }
    });
}

export function applyConfigProfile(profile) {
    const profileConfig = CONFIG_PROFILES[profile];
    if (profileConfig) {
        Object.keys(profileConfig).forEach(key => {
            setConfig(key, profileConfig[key]);
        });
        console.log(`Applied revolutionary configuration profile: ${profile}`);
    } else {
        console.warn(`Unknown revolutionary configuration profile: ${profile}`);
    }
}

// Helper functions remain the same
function getNestedValue(obj, path) {
    return path.split('.').reduce((current, key) => {
        return current && current[key] !== undefined ? current[key] : undefined;
    }, obj);
}

function setNestedValue(obj, path, value) {
    const keys = path.split('.');
    const lastKey = keys.pop();
    const target = keys.reduce((current, key) => {
        if (!current[key] || typeof current[key] !== 'object') {
            current[key] = {};
        }
        return current[key];
    }, obj);
    target[lastKey] = value;
}

